// for loop - used when we know the loop needs to run X times
for (var i = 3; i <= 21; i++) {
    if (i % 2 != 0) {
        console.log(i);
    }
}

// while loop - used when a condition is met for an indeterminate amount of times
var j = 3;
while (j <= 1) {
    console.log(j);
    if (j % 2 != 0) {
        
        j++;
    }
    
}
