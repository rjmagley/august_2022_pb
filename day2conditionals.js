// double equal sign ( == ) is for comparison of two values
// triple equal sign ( === ) is for comparison of value AND type
// && (and) and || (or) can be used to check two or more statements at once
// ! (not) inverts the statement (not true, not false)

var x = 7;
var y = 28;

if (x > 5) {
    if (y > 10) {
        console.log("x is greater than 5, y is greater than 10");
        console.log("that's pretty neat!");
    }
    else {
        console.log("x is greater than 5, y is not greater than 10");
    }
}

