// parameters
// data that changes how a function works
// one parameter - name
// when function is defined, we define parameters
function greetByName(name) {
    // greet someone using their name
    console.log('hello ' + name + ' :3');
}

// when function is called, we supply/provide arguments
greetByName("Alex");
greetByName("Brad");
greetByName("Charlie");
greetByName("Davina");
greetByName("Erek");
greetByName();